	<p class='projet_text'>
Lors de mon Master I, j'ai réalisé un stage de 3 mois. J'ai effectué ce dernier dans l'entreprise <b>TVPaint Development</b>  à Metz (57) dont je vous invite à visiter le site dans la rubrique "Liens". Durant ce stage, j'ai participé à la mise à niveau du logiciel de dessin d’animation graphique <b>TVPaint Animation</b>. 
	</p>
	<p class='presentation_text'>
TVPaint Animation est un logiciel d'animation 2D bitmap, fait par des artistes pour des artistes. Combinant les fonctionnalités de dessin en temps réel et d’animation dans un seul logiciel, TVP Animation centralise tous les processus pour la création de dessins animés. L’interface intuitive de TVPaint Animation intègre directement l’artiste au coeur de son oeuvre afin de se concentrer sur la création, plutôt que sur la technique. 
Conçu pour toutes les résolutions allant de la TV Haute Définition et du film, jusqu’à l’animation pour Internet, TVPaint Animation est une alternative simple et puissante, par rapport à d’autres solutions onéreuses et complexes.
	</p>
	<p class='presentation_text'>
Les principales fonctionnalités de TVPaint Animation sont le dessin en temps réel et l'animation. En effet, il possède un moteur proposant de nombreux outils de dessin entièrement personnalisables afin de pousser les possibilités de création à l'infini. L'animation par images clés permet de générer automatiquement des effets d'animation de tout type. TVPaint Animation possède également une grande gamme d'effets allant des moteurs à particules aux différents flous et autre <i>ToonShading</i>. Il propose également un SDK  pour la création de plug-in.
	</p>
	<p class='presentation_text'>
Durant ce stage, mon travail a été de participer à la mise à niveau du logiciel en implémentant des outils pour la plateforme Linux. J'ai donc travaillé sur le développement de l'outil webcam sous Linux, afin de faire de la gestion de flux vidéo. La gestion de plusieurs webcams a été effectué ainsi que l'ajout d'outils pour le <i>stop-motion</i>. Je me suis ensuite intéressé sur la programmation d'outils Linux concernant l'interface utilisateur avec la bibliothèque <b>Xlib</b>. Enfin, j'ai participé au traitement de flux audio et vidéo avec le développement d'outils de la bibliothèque <b>FFmpeg</b>. 
	</p>	
