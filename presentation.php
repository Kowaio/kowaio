

<h1 class='titre_presentation'>Qui suis-je ?</h1>

<p class='presentation_text'>
Bonjour à tous et bienvenue sur mon site.
</p>
<p class='presentation_text'>
Je m'appelle Guillaume SIORAK, j'ai 24 ans et je suis actuellement salarié de la société <b><a class='a_liens' href="http://www.dotemu.com">DotEmu</a></b> en tant que développeur d'applications mobiles.
Je suis titulaire d'un Baccaulauréat Scientifique, d'un Diplôme 
Universitaire et Technologique (DUT) en Informatique, d'une licence
Mathématique Mécanique et Informatique (MMI) et d'un Master II en Informatique.
</p>
<p class='presentation_text'>
A la fin de mon DUT, j'ai effectué un stage de 2 mois au laboratoire du <b>CReSTIC</b> groupe <b>SIC/MADS</b> à l’IUT de Reims (Marne – 51). J'ai, durant celui-ci, réalisé des applications (C++, Qt, OpenGL) pour un gant de données (Data Glove). Lors de mon Master I, j'ai également réalisé un stage de 3 mois. J'ai effectué ce dernier dans l'entreprise <b><a class='a_liens' href="http://www.tvpaint.com">TVPaint Development</a></b> (Metz – 57). Durant ce stage, j'ai participé à la mise à niveau du logiciel de dessin d’animation graphique <b>TVPaint Animation</b>. 
<br/>
<br/>
J'ai ensuite rejoins l'équipe de <b><a class='a_liens' href="http://www.dotemu.com">DotEmu</a></b> (Paris – 75) lors de mon stage de 6 mois de Master II Informatique. Durant celui-ci, j'ai pu me former à l'environnement MAC ainsi qu'à l'Objective-C afin de réaliser des applications et des jeux sur iPhone/iPad. Je suis actuellement en CDI et l'un des principaux développeurs du pôle iPhone de la société. Vous pouvez retrouver une liste non exhaustive des jeux iPhone que j'ai pu développer sur le site iPhone de la société : <b><a class='a_liens' href="http://iphone.dotemu.com">iphone.dotemu.com</a></b>
</p>
<p class='presentation_text'>
Vous trouverez dans la rubrique "Projets" plus d'informations concernant les stages que j'ai pu suivre ainsi que les projets que j'ai pu réaliser au cours de ma formation.
</p>
<p class='presentation_text'>
Pour de plus amples informations concernant mon cursus ainsi que mes compétences, 
n'hésitez pas à consulter mon Curriculum Vitae que vous trouverez en version PDF (français et anglais), ci-dessous.
</p>
<p class='presentation_text'>
En vous remerciant de votre lecture, je vous souhaite une agréable visite sur mon site.
</p>
<p class='presentation_text'>
Guillaume
