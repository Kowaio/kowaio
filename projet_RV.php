	<p class='projet_text'>
Le dernier module que nous avions était la <b>R</b>éalité <b>V</b>irtuelle. Comme son nom l'indique, ce module se chargeait de nous donner les bases et les principes de cette technique de simulation informatique interactive et immersive.
	</p>
	<p class='presentation_text'>
Le but de ce projet était, toujours à l'aide de C++ et OpenGL, de créer une application permettant de lire une image en niveau de gris ou une image couleur et de créer 2 images homologues résultantes de celle-ci mais légérement décalées avec l'image de gauche en rouge et l'image de droite couleur cyan. Cela permet ainsi de créer ce que l'on appelle un anaglyphe et donc de rendre compte de la notion de stéréoscopie. Ainsi, notre cerveau est trompé et saisit une idée de relief grâce à des lunettes à anaglyphes.
	</p>	
