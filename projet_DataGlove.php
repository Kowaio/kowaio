	<p class='projet_text'>
Pendant 2 mois, d'avril à juin 2007, j'ai effectué un stage au laboratoire du <b>CReSTIC</b> groupe <b>SIC/MADS</b> à l’IUT de Reims. J'ai, durant celui-ci, réalisé des applications (C++, Qt, OpenGL) pour un gant de données (Data Glove).
	</p>
	<p class='presentation_text'>
Le sujet de ce stage consistait en l’exploitation d’un outil d’immersion, le gant de données (Data Glove). Une application de démonstration représentant un squelette articulé de la main a été réalisée afin de permettre un rendu en temps réel de l’utilisation d’un tel outil. Elle permet donc de retranscrire les mouvements des doigts de l’utilisateur à l’écran. Une deuxième application a également été développée, présentant une utilisation pratique et divertissante du matériel dans le jeu de « Pierre – Feuille – Ciseaux ».
	</p>		
