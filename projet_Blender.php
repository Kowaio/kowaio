	<p class='projet_entete'>
	<b class='u_projet'>Plateforme</b> : Windows / Mac OS X
	</p>
	
	<p class='projet_text'>
Durant cette 2ème année de Master, nous avons eu un module de modélisation et animation de scène 3D avec le logiciel <b>Blender</b>. Nous avons ainsi, pendant tout un semestre, appris les bases de ce logiciel afin de modéliser des objets, les intégrer dans une scène et animer cette scène avec des techniques bien spécifiques.
	</p>
	<p class='presentation_text'>
Dans ce module, nous avions un projet à réaliser. Le but était de modéliser une scène entière afin de créer un court métrage d'animation, comportant un ou plusieurs personnages animés. Nous avons choisi le thème du Far-West et donc de la scène célèbre du saloon où interagiraient nos différents éléments. 
	</p>
