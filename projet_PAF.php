	<p class='projet_entete'>
	<b class='u_projet'>Développement</b> : Mars 2007 en partenariat avec Dimitri Lenoble
	</p>
	<p class='projet_entete'>
	<b class='u_projet'>Langage / Bibliothèque</b> : C++ / OpenGL sous Microsoft Visual Studio
	</p>
	<p class='projet_entete'>
	<b class='u_projet'>Plateforme</b> : Windows
	</p>
	
	<p class='projet_text'>
Lors du 2ème semestre de notre 2ème année de DUT Informatique, nous avons eu un module d'Imagerie Numérique nommé <b>A</b>nimation et <b>A</b>cquisition de <b>M</b>ouvement. Le but de celui-ci était de nous familiariser avec les outils qui fondent ce domaine.	
	</p>
	<p class='presentation_text'>
Pour le projet de ce module, nous avons choisi de modéliser un tissu au cours du temps auquel serait appliqué certaines forces. Nous avons donc repris un moteur physique nommé <b>PAF</b> et nous l'avons modifié afin d'intégrer nos tissus.
	</p>	
	<p class='presentation_text'>
Un tissu est donc composé de particules mobiles. Nous avons également défini des ressorts afin de faire tenir les particules entre elles. Des obstacles ont été créés afin de voir la réaction de ce tissu sur ces derniers.
	</p>	
	<p class='presentation_text'>
Comme je le disais précédemment, le tissu se voit appliquer certaines forces. Il réagit au cours du temps, mais surtout en fonction du vent. Cette force a été ajouté afin de voir l'évolution de ce tissu face à cette force.
	</p>			
