	<p class='projet_text'>
Un autre module que nous avions à l'IUT était la <b>P</b>rogrammation <b>G</b>raphique <b>A</b>vancée. Le but de ce module était de se familiariser avec la bibliothèque <b>OpenGL</b> en suivant des TP.
	</p>
	<p class='presentation_text'>
Le but de ce projet était de charger, lire et afficher un fichier "<i>.mesh</i>" en C++ à l'aide de la bibliothèque <b>OpenGL</b>. Des options étaient alors renseignées telles que la gestion de lampe avec <b>OpenGL</b>, la création d'une <i>Bounding Box</i> pour délimiter le maillage, l'activation ou non de l'affichage des normales en chaque sommet etc...
	</p>	
